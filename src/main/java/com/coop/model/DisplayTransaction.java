package com.coop.model;

public class DisplayTransaction {
	private String ga_sender_name;
	private String ga_sender_accno;
	private String ga_receiver_accno;
	private String ga_payee_amount;
	private String ga_payee_remark;
	public String getGa_sender_name() {
		return ga_sender_name;
	}
	public void setGa_sender_name(String ga_sender_name) {
		this.ga_sender_name = ga_sender_name;
	}
	public String getGa_sender_accno() {
		return ga_sender_accno;
	}
	public void setGa_sender_accno(String ga_sender_accno) {
		this.ga_sender_accno = ga_sender_accno;
	}
	
	public String getGa_receiver_accno() {
		return ga_receiver_accno;
	}
	public void setGa_receiver_accno(String ga_receiver_accno) {
		this.ga_receiver_accno = ga_receiver_accno;
	}
	public String getGa_payee_amount() {
		return ga_payee_amount;
	}
	public void setGa_payee_amount(String ga_payee_amount) {
		this.ga_payee_amount = ga_payee_amount;
	}
	public String getGa_payee_remark() {
		return ga_payee_remark;
	}
	public void setGa_payee_remark(String ga_payee_remark) {
		this.ga_payee_remark = ga_payee_remark;
	}
	

}
